CREATE TABLE TEMPS( 
     CELCIUS NUMBER(*,2), 
     FAHRENHEIT NUMBER(*,2) 
); 

CREATE OR REPLACE FUNCTION CELSIUS_PARA_FAHRENHEIT (CELCIUS NUMBER) RETURN NUMBER IS FAHRENHEIT NUMBER; 
BEGIN 
     FAHRENHEIT := (CELCIUS * 1.8) + 32; 
     RETURN FAHRENHEIT; 
END CELSIUS_PARA_FAHRENHEIT; 

CREATE OR REPLACE TRIGGER SET_TEMPS BEFORE INSERT OR UPDATE ON TEMPS 
FOR EACH ROW 
BEGIN 
     :NEW.FAHRENHEIT := CELSIUS_PARA_FAHRENHEIT(:NEW.CELCIUS); 
END; 

