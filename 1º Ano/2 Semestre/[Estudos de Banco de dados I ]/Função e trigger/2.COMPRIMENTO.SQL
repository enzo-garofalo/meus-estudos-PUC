CREATE TABLE PESSOA( 
 DATA_NASCIMENTO DATE, 
 IDADE INTEGER 
); 

CREATE OR REPLACE FUNCTION CALCULAR_IDADE (DATA_NASCIMENTO DATE) RETURN NUMBER IS IDADE NUMBER; 
BEGIN 
     IDADE := FLOOR(MONTHS_BETWEEN(SYSDATE, DATA_NASCIMENTO)/12); 
     RETURN IDADE; 
END CALCULAR_IDADE; 

CREATE OR REPLACE TRIGGER CALCULA BEFORE INSERT ON PESSOA 
FOR EACH ROW 
BEGIN 
     :NEW.IDADE := CALCULAR_IDADE(:NEW.DATA_NASCIMENTO); 
END; 

